// 스토리 파일 생성
function generateStoryFiles() {
  const baseDir = 'C:/Users/dbals/VibeCoding/HearO_web/public/assets/prerendered/stories';
  const worldviews: WorldviewType[] = ['fantasy', 'sports', 'idol', 'sf', 'zombie', 'spy'];
  const grades: GradeType[] = ['perfect', 'good', 'normal'];

  let totalCreated = 0;

  for (const exercise of MISSING_EXERCISES) {
    for (const worldview of worldviews) {
      for (const grade of grades) {
        const story = STORIES[exercise][worldview][grade];
        const filePath = path.join(baseDir, worldview, `${exercise}_${grade}.txt`);

        // 디렉토리 확인/생성
        const dir = path.dirname(filePath);
        if (!fs.existsSync(dir)) {
          fs.mkdirSync(dir, { recursive: true });
        }

        // 파일 작성
        fs.writeFileSync(filePath, story, 'utf-8');
        console.log(`Created: ${filePath}`);
        totalCreated++;
      }
    }
  }

  console.log(`\n✅ Total ${totalCreated} story files created!`);
}

// 메인 실행
generateStoryFiles();
